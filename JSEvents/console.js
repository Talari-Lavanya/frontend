// Example of using console.log() for debugging

function addNumbers(a, b) {
    console.log("Input values:", a, b); // Debugging input values
    const result = a + b;
    console.log("Calculated result:", result); // Debugging the result
    return result;
  }
  
  addNumbers(5, 3); // Output: Input values: 5 3, Calculated result: 8
  